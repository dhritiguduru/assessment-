package com.book;

import java.util.Arrays;

public class Book {
	String bookID;
	String title;
	String author;
	String category;
	float price;

	// Parameterized constructor of book class
	public Book(String bookID, String title, String author, String category, float price) {
		super();
		this.bookID = bookID;
		this.title = title;
		this.author = author;
		this.category = category;
		this.price = price;
	}

	
	

}
